/*  LMF_RUN_IOV  */
CREATE OR REPLACE VIEW  LMF_SEQUENCE_DAT AS
SELECT DISTINCT
  RUN_IOV_ID  ,
  SEQUENCE_NUM ,
  MIN(SUBRUN_START) SEQ_START ,
  MAX(SUBRUN_END) SEQ_END
FROM LMF_RUN_IOV 
GROUP BY RUN_IOV_ID, SEQUENCE_NUM ;

